        internal static string getProductName(Program program)
        {
            // Get the version number of the process
            int version = Process.GetCurrentProcess().MainModule.FileVersionInfo.ProductMajorPart;

            // If we're running (<16), use a pre-made String.
            if (OfficeVersions.ContainsKey(version))
            {
                return programNames[program] + " " + OfficeVersions[version];
            }

            // If we are running (16), use a magical detection algorithm full of nested-if statements.
            if (version == 16)
            {
                RegistryKey key = Registry.CurrentUser.OpenSubKey(@"Software\Microsoft\Office\16.0\Common\Licensing\BootTimeSkuOverride");

                // If the BootTimeSkuOverride registry key does not exist, we've failed!
                if (key == null) return null;

                // For every product in our database...
                foreach (Product product in products)
                {
                    // Enumerate over the specified program only
                    if (product.program != program) continue;

                    // Check if the UUID exists in BootTimeSkuOverride
                    var skuOverrideObject = key.GetValue($"{{{product.uuid}}}");

                    // If a SKU Override exists...
                    if (skuOverrideObject != null)
                    {
                        // Convert the registry key into a String
                        string skuOverride = skuOverrideObject.ToString();

                        // If our SKUs list contains the override key
                        if (SKUs.ContainsKey(skuOverride))
                        {
                            string name = SKUs[skuOverride];

                            // Return the name of the key!
                            return name;
                        }
                    }
                }
            }

            return "Unknown Product SKU";
        }
